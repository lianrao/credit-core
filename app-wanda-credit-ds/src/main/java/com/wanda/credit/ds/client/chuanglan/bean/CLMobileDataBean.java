package com.wanda.credit.ds.client.chuanglan.bean;

public class CLMobileDataBean {
	private String status;
	private String lastTime;
	private String area;
	private String numberType;
	private String mobile;
	private String chargesStatus;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getLastTime() {
		return lastTime;
	}
	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getNumberType() {
		return numberType;
	}
	public void setNumberType(String numberType) {
		this.numberType = numberType;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getChargesStatus() {
		return chargesStatus;
	}
	public void setChargesStatus(String chargesStatus) {
		this.chargesStatus = chargesStatus;
	}
}
