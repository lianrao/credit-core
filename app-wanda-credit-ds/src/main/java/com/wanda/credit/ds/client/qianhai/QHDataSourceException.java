package com.wanda.credit.ds.client.qianhai;

public class QHDataSourceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public QHDataSourceException() {
		super();
	}

	public QHDataSourceException(String message) {
		super(message);
	}

	public QHDataSourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public QHDataSourceException(Throwable cause) {
		super(cause);
	}
}