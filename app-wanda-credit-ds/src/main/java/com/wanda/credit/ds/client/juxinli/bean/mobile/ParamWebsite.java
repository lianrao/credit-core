package com.wanda.credit.ds.client.juxinli.bean.mobile;

public class ParamWebsite {
	
	private String website_en_name;//网站英文名称
	private String category_en_name;//网站分类英文名称
	
	
	public String getWebsite_en_name() {
		return website_en_name;
	}
	public void setWebsite_en_name(String website_en_name) {
		this.website_en_name = website_en_name;
	}
	public String getCategory_en_name() {
		return category_en_name;
	}
	public void setCategory_en_name(String category_en_name) {
		this.category_en_name = category_en_name;
	}
	
	public String toString() {
		return "ParamWebsite [website_en_name=" + website_en_name
				+ ", category_en_name=" + category_en_name + "]";
	}
	
	
	
	
	

}
